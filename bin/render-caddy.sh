#!/usr/bin/env bash
# Regenerate the Caddyfile from registry.json, then reload Caddy.
# Idempotent. Never hand-edit the Caddyfile; edit the registry and re-render.
source "$(dirname "$(readlink -f "$0")")/lib.sh"

ensure_registry

{
  echo "# AUTO-GENERATED by bin/render-caddy.sh from registry.json. Do not edit by hand."
  echo "{"
  echo "    auto_https off"
  echo "    admin off"
  echo "}"
  echo ""
  echo ":$CADDY_PORT {"
  echo "    encode gzip"
  echo ""
  # Public host-based routes (Cloudflare Tunnel -> here). All tunnel traffic arrives on this same
  # :CADDY_PORT with the original public Host header, so we match on host and serve the app at the
  # subdomain ROOT. These come FIRST: Caddy `handle` blocks are mutually exclusive and first-match
  # wins by source order, so a public-host request is served here while internal Tailscale requests
  # (different Host) fall through to the path routes below. Only `static` apps can be served at root;
  # `next` apps bake basePath:'/<slug>' and would 404 at a subdomain root (deferred).
  for slug in $(reg_list_slugs); do
    reg_is_public "$slug" || continue
    type="$(reg_field "$slug" type)"
    host="$slug.$PUBLIC_DOMAIN"
    if [ "$type" = "static" ]; then
      echo "    @$slug-pub host $host"
      echo "    handle @$slug-pub {"
      echo "        root * $APPS_DIR/$slug"
      echo "        file_server"
      echo "    }"
      echo ""
    else
      log "skip public host for '$slug' (type=$type): only static apps can serve at a subdomain root"
    fi
  done
  # Per-experiment routes.
  for slug in $(reg_list_slugs); do
    type="$(reg_field "$slug" type)"
    port="$(reg_field "$slug" port)"
    if [ "$type" = "worker" ]; then
      # Workers are background processes with no HTTP port — nothing to route.
      continue
    elif [ "$type" = "static" ]; then
      # Static: strip the prefix; serve files from the app dir (relative asset paths).
      echo "    handle_path /$slug/* {"
      echo "        root * $APPS_DIR/$slug"
      echo "        file_server"
      echo "    }"
    else
      # Next / dynamic: preserve the prefix so it lines up with basePath.
      echo "    handle /$slug/* {"
      echo "        reverse_proxy 127.0.0.1:$port"
      echo "    }"
    fi
    echo ""
  done
  # Landing page at the root. Once the home Next app is built (server.js exists), reverse-proxy
  # to it on the reserved HOME_PORT; otherwise fall back to serving the static $HOME_DIR/index.html.
  # Keying off the build artifact makes the flip automatic and safe (no '/' 502 before it's up).
  if [ -f "$HOME_DIR/.next/standalone/server.js" ]; then
    echo "    handle {"
    echo "        reverse_proxy 127.0.0.1:$HOME_PORT"
    echo "    }"
  else
    echo "    handle {"
    echo "        root * $HOME_DIR"
    echo "        file_server"
    echo "    }"
  fi
  echo "}"
  echo ""
  echo "# Plain-HTTP (port 80, via tailscale serve --http=80) -> HTTPS redirect."
  echo ":$REDIR_PORT {"
  echo "    redir https://$TS_HOST{uri} permanent"
  echo "}"
} > "$CADDYFILE"

log "Rendered $CADDYFILE"

# Reload Caddy if its service is running; ignore if not yet installed.
if systemctl --user is-active --quiet caddy-experiments 2>/dev/null; then
  systemctl --user reload caddy-experiments 2>/dev/null \
    || systemctl --user restart caddy-experiments
  log "Reloaded caddy-experiments"
else
  log "caddy-experiments not running yet (skipped reload)"
fi
